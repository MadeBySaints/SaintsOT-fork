local SELECTED_BG = "#3a5a3a"
local UNSELECTED_BG = "#00000000"
local GREEN = "#4caf50"

local selectedEntry = nil
local selectedRow = nil

local clearSelection = function()
  if selectedRow then
    selectedRow:setBackgroundColor(UNSELECTED_BG)
  end
  selectedEntry = nil
  selectedRow = nil
  getButton:setEnabled(false)
  getButton:setImageColor("#ffffff")
end

local selectRow = function(row, entry)
  if selectedRow then
    selectedRow:setBackgroundColor(UNSELECTED_BG)
  end
  row:setBackgroundColor(SELECTED_BG)
  selectedRow = row
  selectedEntry = entry
  getButton:setEnabled(true)
  getButton:setImageColor(GREEN)
end

local buildRowsForCategory = function(categoryKey, searchQuery)
  clearSelection()
  listPanel:destroyChildren()
  local items = itemsByCategory[categoryKey]
  if not items then return end

  local query = (searchQuery or ""):lower()

  for _, entry in ipairs(items) do
    if query == "" or entry.name:lower():find(query, 1, true) then
      local row = setupUI(rowOtml, listPanel)
      row:setBackgroundColor(UNSELECTED_BG)
      row.icon:setItem(Item.create(entry.id, 1))
      row.name:setText(entry.name)

      local onRowClick = function()
        selectRow(row, entry)
        return true
      end
      row.onClick = onRowClick
    end
  end
end

getButton.onClick = function()
  if not selectedEntry then return end
  say("/i " .. selectedEntry.name .. ", " .. storage.browserGiveAmount)
end

categoryRow.category.onOptionChange = function(widget, text, data)
  searchRow.search:setText("")
  buildRowsForCategory(data, "")
end

searchRow.search.onTextChange = function(widget, text)
  local data = categoryRow.category:getCurrentOption().data
  buildRowsForCategory(data, text)
end

-- selecting index 1 fires onOptionChange (set above), which builds the
-- initial row list - no separate manual build call needed here.
categoryRow.category:setCurrentIndex(1)

